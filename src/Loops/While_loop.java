package Loops;

public class While_loop {
    public static void main(String[] args) {

        //This is the while loop
        int num =1; //initialization
        while(num<=10){ //condition
            //code to be executed
            System.out.println(num);


            num++;//updation
        }

        System.out.println();

        int count =500;

        while (count>=200){
            System.out.println(count);

            count--;
        }
    }
}
